package week1.day3;

import java.lang.*;
import java.util.*;

class ConcatenateIntegers {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        int c = a * 10 + b;
        System.out.println(c);
    }
}