# User function Template for python3

# Define the Person class
class Person:
    def __init__(self):
        self.name = "Geeks"
        self.age = 10

    def get_name(self):
        return self.name

    def get_age(self):
        return self.age

    def set_name(self, name):
        self.name = name

    def set_age(self, age):
        self.age = age
